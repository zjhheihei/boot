void bsp_blink_cfg(void)
{
    M0P_SYSCTRL->PERI_CLKEN0_f.GPIO = 1;
    M0P_GPIO->PCDIR_f.PC13 = 0;   
}
//-----------------------------------------------------------------------------
void bsp_led_light_on(void)
{
    M0P_GPIO->PCOUT_f.PC13 = 1; 
}
//------------------------------------------------------------------------------
void bsp_led_light_off(void)
{
    M0P_GPIO->PCOUT_f.PC13 = 0;   
}
